/* Pruebas y códigos
 * Aquí irán las siguientes informaciones importantes a tener en cuenta en el código.
 
 * Igual: ==
 * Dos signos de igual (==) significan que es igual o representa lo mismo.
 * Ejemplo: if (a == b) { // código a ejecutar si a es igual a b }
 
 * Distinto: !=
 * La exclamación seguida de un igual (!=) declara que es diferente.
 * Ejemplo: if (a != b) { // código a ejecutar si a es diferente de b }
 
 * Mayor: >
 * Este símbolo declara que es mayor.
 * Ejemplo: if (a > b) { // código a ejecutar si a es mayor que b }
 
 * Menor: <
 * Este símbolo declara que es menor.
 * Ejemplo: if (a < b) { // código a ejecutar si a es menor que b }
 
 * Mayor o igual: >=
 * Este símbolo declara que es mayor o igual.
 * Ejemplo: if (a >= b) { // código a ejecutar si a es mayor o igual que b }
 
 * Menor o igual: <=
 * Este símbolo declara que es menor o igual.
 * Ejemplo: if (a <= b) { // código a ejecutar si a es menor o igual que b }
 
 * O: ||
 * Los dos palos verticales (||) representan el "o", es decir, si ocurre algo o no ocurre.
 * Ejemplo: if (a > b || a < c) { // código a ejecutar si a es mayor que b o menor que c }
 
 * Y: &&
 * Los dos signos de ampersand (&&) representan el "y", es decir, si ocurre algo y también ocurre otra cosa.
 * Ejemplo: if (a > b && a < c) { // código a ejecutar si a es mayor que b y menor que c }
 
 * If: 
 * Declara que si ocurre algo, se ejecuta un bloque de código.
 * Ejemplo: if (numerodesaltos >= 4) { // código a ejecutar si numerodesaltos es mayor o igual a 4 }
 
 * Else: 
 * Se usa junto con if para declarar un bloque de código que se ejecuta si la condición del if no se cumple.
 * Ejemplo: if (a == b) { // código si a es igual a b } else { // código si a no es igual a b }
 
 * Else if: 
 * Se usa para especificar una nueva condición si la primera condición if no se cumple.
 * Ejemplo: if (a == b) { // código si a es igual a b } else if (a > b) { // código si a es mayor que b } else { // código si ninguna de las condiciones anteriores se cumple }
 
 * Resumen de if, else if y else:
 * - if: Evalúa una condición y ejecuta el bloque de código si la condición es verdadera.
 * - else if: Evalúa una nueva condición si la condición anterior no era verdadera. Puedes tener múltiples else if.
 * - else: Ejecuta un bloque de código si ninguna de las condiciones anteriores es verdadera. No lleva condición y solo puede haber uno.
 */

